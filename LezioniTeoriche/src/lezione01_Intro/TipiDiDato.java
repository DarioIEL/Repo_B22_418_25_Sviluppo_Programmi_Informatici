package lezione01_Intro;

public class TipiDiDato {
	public static void main(String[] args) {
	
	//TIPI di DATO PRIMITIVI
		
	//Numeri interi
	
		byte anni = 36; //8bit
		
		short litriPiscina = -30000; //16bit
		
		int soldiBanca = 1000000000; //32bit -- PREFERITO
		
		long etaTerra = 4000000000L; //64bit
		
	//Numeri con la virgola
		
		float piGreco = 3.14f;
		
		double nepero = 2.71; //PREFERITO 
		
		
	//Tipi Boolean (solitamente 1 bit)
		
		boolean v = true;
		
		boolean f = false;
		
		
	//Tipi caratteri
		char lettera = 'q';
		
	//ATT: la string non Ã¨ un tipo primitivo
		String mioNome = "ciao";
		
	//Questi sotto non sono dei primitivi, sono tipi complessi, oggetti. Iniziano con la maiusc come le stringhe
		Integer numeroInt = 100;
		Double numero = 8.9;		
		Boolean acceso = true;

	}
}
